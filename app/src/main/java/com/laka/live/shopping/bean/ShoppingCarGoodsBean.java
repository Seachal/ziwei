package com.laka.live.shopping.bean;

import android.text.TextUtils;

import java.io.Serializable;

/**
 * Created by chenjiawei on 2016/1/4.
 */
public class ShoppingCarGoodsBean implements Serializable {
    private String itemId;
    private String goodsId;
    private String salePrice;
    private String goodsCount;
    private String description;
    private String title;
    private String thumbUrl;
    private String surplusCount;
    private String specName;
    private String surplusSeconds;
    private String isPanicShopping;
    private String invalidState;

    public String getItemId() {
        return itemId;
    }

    public void setItemId(String itemId) {
        this.itemId = itemId;
    }

    public String getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(String goodsId) {
        this.goodsId = goodsId;
    }

    public String getSalePrice() {
        return salePrice;
    }

    public void setSalePrice(String salePrice) {
        this.salePrice = salePrice;
    }

    public String getGoodsCount() {
        if(TextUtils.isEmpty(goodsCount)){
            return "0";
        }
        return goodsCount;
    }

    public void setGoodsCount(String goodsCount) {
        this.goodsCount = goodsCount;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getThumbUrl() {
        return thumbUrl;
    }

    public void setThumbUrl(String thumbUrl) {
        this.thumbUrl = thumbUrl;
    }

    public String getSurplusCount() {
        return surplusCount;
    }

    public void setSurplusCount(String surplusCount) {
        this.surplusCount = surplusCount;
    }

    public String getSpecName() {
        return specName;
    }

    public void setSpecName(String specName) {
        this.specName = specName;
    }

    public String getIsPanicShopping() {
        return isPanicShopping;
    }

    public void setIsPanicShopping(String isPanicShopping) {
        this.isPanicShopping = isPanicShopping;
    }

    public String getInvalidState() {
        return invalidState;
    }

    public void setInvalidState(String invalidState) {
        this.invalidState = invalidState;
    }

    public String getSurplusSeconds() {
        return surplusSeconds;
    }

    public void setSurplusSeconds(String surplusSeconds) {
        this.surplusSeconds = surplusSeconds;
    }
}
